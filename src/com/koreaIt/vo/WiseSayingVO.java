package com.koreaIt.vo;

public class WiseSayingVO {
	String content;
	String author;
	public WiseSayingVO(String content, String author) {
		super();
		this.content = content;
		this.author = author;
	}
	public String getContent() {
		return content;
	}
	public String getAuthor() {
		return author;
	}
	
	

}
